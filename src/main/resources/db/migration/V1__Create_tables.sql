/*
 * Ogel Submission DB Script
 */
CREATE TABLE IF NOT EXISTS LOCAL_OGEL_SUBMISSION (
  ID                  BIGSERIAL PRIMARY KEY,
  USER_ID             TEXT,
  ADMIN_USER_ID       TEXT,
  OGEL_TYPE           TEXT,
  MODE                TEXT,
  STAGE               TEXT,
  STATUS              TEXT,
  CALLED_BACK         BOOLEAN,
  CUSTOMER_REF        TEXT,
  SITE_REF            TEXT,
  SPIRE_REF           TEXT,
  ROLE_UPDATE         BOOLEAN,
  ROLE_UPDATED        BOOLEAN,
  FAIL_REASON         TEXT,
  SUBMISSION_REF      TEXT,
  CALLBACK_URL        TEXT,
  CALLBACK_FAIL_COUNT INTEGER,
  FIRST_FAIL          TIMESTAMP,
  LAST_FAIL           TIMESTAMP,
  LAST_FAIL_MESSAGE   TEXT,
  JSON                TEXT,
  LITE_JWT_USER       TEXT,
  CREATED             TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);